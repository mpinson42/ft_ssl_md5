#include "libft.h"

wchar_t	ft_uni_strcmp(wchar_t *s1, wchar_t *s2)
{
	int		i;

	i = 0;
	while (s1[i] != '\0' && s2[i] != '\0' && s1[i] == s2[i])
		i++;
	return (s1[i] - s2[i]);
}
